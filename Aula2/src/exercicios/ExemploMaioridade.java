package exercicios;

public class ExemploMaioridade {

	public static void main(String[] args) {
		int idade = 10;
		
		if(idade == 18) {
			System.out.println("Pessoa com 18 anos");	
		}
		
		else if (idade > 18) {
			System.out.println("Pessoa com mais de 18 anos");
		}
			else {
				System.out.println("Pessoa com menos de 18 anos");
			}
		
		
	}

}
